import turtle as t
"""Raymond Healy"""

"""---------------------------------------------------------------------"""
def init():
    t.speed(10)
    t.up()
    
"""---------------------------------------------------------------------"""

"""---------------------------------------------------------------------"""
def square ():
    t.down()
    t.forward(100)
    t.left(90)
    t.forward(100)
    t.left(90)
    t.forward(100)
    t.left(90)
    t.forward(100)
    t.left(90)
    t.up()
"""---------------------------------------------------------------------"""

"""---------------------------------------------------------------------"""
def triangle ():
    t.down()
    t.forward(100)
    t.left(120)
    t.forward(100)
    t.left(120)
    t.forward(100)
    t.left(120)
    t.up()
"""---------------------------------------------------------------------"""

"""---------------------------------------------------------------------"""
def pentagon ():
    t.down()
    t.forward(100)
    t.left(72)
    t.forward(100)
    t.left(72)
    t.forward(100)
    t.left(72)
    t.forward(100)
    t.left(72)
    t.forward(100)
    t.left(72)
    t.up()
"""---------------------------------------------------------------------"""

"""---------------------------------------------------------------------"""
def main ():
    init()
    square ()
    triangle ()
    pentagon ()
    t.left (120)
    square ()
    triangle ()
    pentagon ()
    t.left (120)
    square ()
    triangle ()
    pentagon ()
    t.left (120)
    t.done()
"""---------------------------------------------------------------------"""

main()
